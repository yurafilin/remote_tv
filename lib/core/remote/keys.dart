/// Brand-independent remote keys.
///
/// Every [RemoteDriver] maps the subset it supports to its own wire format,
/// so the remote UI can be rendered from a driver's capabilities alone.
enum RemoteKey {
  power,
  home,
  back,
  up,
  down,
  left,
  right,
  ok,
  volumeUp,
  volumeDown,
  mute,
  channelUp,
  channelDown,
  playPause,
  rewind,
  fastForward,
  stop,
  replay,
  info,
  menu,
  guide,
  input,
  exit,
  digit0,
  digit1,
  digit2,
  digit3,
  digit4,
  digit5,
  digit6,
  digit7,
  digit8,
  digit9,
}
